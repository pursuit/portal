CREATE TABLE mutations (
	id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	user_id INT NOT NULL,
	reference_id INT NOT NULL,
	reference_type VARCHAR(255) NOT NULL,
	amount INT NOT NULL,
	created_at TIMESTAMP NOT NULL,
	CONSTRAINT fk_user
		FOREIGN KEY(user_id)
		REFERENCES users(id)
		ON DELETE NO ACTION
);
